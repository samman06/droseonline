export class GroupFormComponent implements OnInit, OnChanges {
  @Input() initialValue: any = {};
  @Input() title = 'Add Group';
  @Input() subtitle = 'Create a weekly group with teacher, subject, and grade';
  @Input() submitText = 'Create Group';
  @Output() save = new EventEmitter<any>();
  @Output() cancel = new EventEmitter<void>();

  form!: FormGroup;
  submitting = false;

  readonly days = ['monday','tuesday','wednesday','thursday','friday','saturday','sunday'];
  readonly grades = ['Grade 1','Grade 2','Grade 3','Grade 4','Grade 5','Grade 6','Grade 7','Grade 8','Grade 9','Grade 10','Grade 11','Grade 12'];
  courses: any[] = [];
  selectedCourse: any = null;
  scheduleConflicts: any[] = [];
  checkingConflicts = false;

  constructor(
    private fb: FormBuilder, 
    private courseService: CourseService,
    private groupService: GroupService,
    private toastService: ToastService,
    private confirmation: ConfirmationService,
    private router: Router,
    private translate: TranslateService
  ) {}

  ngOnInit(): void {
    // Initialize form with default values
    this.form = this.fb.group({
      name: [{value: '', disabled: true}], // Auto-generated, disabled like code
      code: [{value: '', disabled: true}], // Auto-generated, not required
      course: ['', [Validators.required]],  // Required field
      gradeLevel: ['Grade 9', [Validators.required]],
      schedule: this.fb.array([this.createScheduleSlot()]),
      pricePerSession: [0, [Validators.min(0)]]
    });

    // Load courses with teacher and subject info
    this.loadCourses();

    // Watch for course changes to update selected course and check conflicts
    this.form.get('course')?.valueChanges.pipe(distinctUntilChanged()).subscribe((courseId) => {
      this.selectedCourse = this.courses.find(c => c._id === courseId);
      this.autoGenerateGroupName();
      this.checkScheduleConflicts();
    });
    
    // Watch for grade level changes to auto-generate name
    this.form.get('gradeLevel')?.valueChanges.pipe(distinctUntilChanged()).subscribe(() => {
      this.autoGenerateGroupName();
    });
    
    // Watch for schedule changes to check for conflicts and update name
    this.form.get('schedule')?.valueChanges.pipe(debounceTime(500)).subscribe(() => {
      this.autoGenerateGroupName();
      this.checkScheduleConflicts();
    });
    
    // Populate form if initialValue is already available
    if (this.initialValue && Object.keys(this.initialValue).length > 0) {
      this.updateFormValues();
    }
  }

  ngOnChanges(changes: SimpleChanges): void {
    // Update form when initialValue changes (async data loading)
    if (changes['initialValue'] && this.form && changes['initialValue'].currentValue) {
      const current = changes['initialValue'].currentValue;
      if (current && Object.keys(current).length > 0) {
        this.updateFormValues();
      }
    }
  }

  private loadCourses(): void {
    this.courseService.getCourses({ page: 1, limit: 100 }).subscribe({
      next: res => {
        if (res.success) {
          // Handle different response structures
          if (res.data && (res.data as any).courses) {
            this.courses = (res.data as any).courses;
          } else if (Array.isArray(res.data)) {
            this.courses = res.data;
          } else {
            this.courses = [];
          }
          
          console.log('Loaded courses:', this.courses.length);
          
          // If we have initial data, update selected course
          const initialCourseId = this.initialValue?.course?._id || this.initialValue?.course;
          if (initialCourseId && this.courses.length > 0) {
            this.selectedCourse = this.courses.find(c => c._id === initialCourseId);
            console.log('Selected course:', this.selectedCourse);
          }
        } else {
          this.courses = [];
        }
      },
      error: err => { 
        console.error('Failed to load courses:', err); 
        this.courses = []; 
        this.toastService.error(this.translate.instant('groups.failedToLoadCourses'));
      }
    });
  }

  private createScheduleSlot(slot?: any): FormGroup {
    return this.fb.group({
      day: [slot?.day || 'saturday', Validators.required],
      startTime: [slot?.startTime || '10:00', Validators.required],
      endTime: [slot?.endTime || '12:00', Validators.required]
    });
  }

  private updateFormValues(): void {
    if (!this.initialValue) return;
    
    // Extract course ID
    const courseId = this.initialValue.course?._id || this.initialValue.course || '';
    
    // Update basic form fields (don't set name, it will be auto-generated)
    this.form.patchValue({
      code: this.initialValue.code || '',
      course: courseId,
      gradeLevel: this.initialValue.gradeLevel || 'Grade 9',
      pricePerSession: this.initialValue.pricePerSession ?? 0
    });
    
    // Update schedule array
    if (this.initialValue.schedule && Array.isArray(this.initialValue.schedule) && this.initialValue.schedule.length > 0) {
      const scheduleArray = this.form.get('schedule') as FormArray;
      scheduleArray.clear();
      this.initialValue.schedule.forEach((slot: any) => {
        scheduleArray.push(this.createScheduleSlot(slot));
      });
    }
    
    // Update selected course if courses are already loaded
    if (courseId && this.courses.length > 0) {
      this.selectedCourse = this.courses.find(c => c._id === courseId);
    }
    
    // Auto-generate the name based on course, grade, and schedule
    // This ensures the name is always current in edit mode
    this.autoGenerateGroupName();
  }

  get schedule(): FormArray { return this.form.get('schedule') as FormArray; }
  addSlot(): void { this.schedule.push(this.createScheduleSlot()); }
  removeSlot(i: number): void { this.schedule.removeAt(i); }

  autoGenerateGroupName(): void {
    // Get the required data
    const course = this.selectedCourse;
    const gradeLevel = this.form.get('gradeLevel')?.value;
    const scheduleArray = this.form.get('schedule')?.value || [];
    
    // Check if we have enough data to generate a name
    if (!course || !course.subject || !gradeLevel || scheduleArray.length === 0) {
      return;
    }
    
    // Extract subject name
    const subjectName = course.subject.name || 'Subject';
    
    // Get unique days from schedule (abbreviated: Sat, Mon, etc.)
    const dayAbbreviations: { [key: string]: string } = {
      'monday': 'Mon',
      'tuesday': 'Tue',
      'wednesday': 'Wed',
      'thursday': 'Thu',
      'friday': 'Fri',
      'saturday': 'Sat',
      'sunday': 'Sun'
    };
    
    const uniqueDays = [...new Set(scheduleArray.map((slot: any) => slot.day))];
    const daysList = uniqueDays
      .map((day: any) => dayAbbreviations[day.toLowerCase()] || day)
      .join('/');
    
    // Generate the name: "Subject - Grade - Days"
    const generatedName = `${subjectName} - ${gradeLevel} - ${daysList}`;
    
    // Always update the name field (it's disabled, so user can't edit it anyway)
    this.form.patchValue({ name: generatedName }, { emitEvent: false });
  }

  checkScheduleConflicts(): void {
    const courseId = this.form.get('course')?.value;
    const schedule = this.form.get('schedule')?.value;

    if (!courseId || !schedule || schedule.length === 0) {
      this.scheduleConflicts = [];
      return;
    }

    // Validate schedule times
    const hasInvalidSchedule = schedule.some((slot: any) => 
      !slot.day || !slot.startTime || !slot.endTime
    );
    
    if (hasInvalidSchedule) {
      this.scheduleConflicts = [];
      return;
    }

    this.checkingConflicts = true;
    const excludeGroupId = this.initialValue?._id || this.initialValue?.id;

    this.groupService.checkScheduleConflict(courseId, schedule, excludeGroupId).subscribe({
      next: (response: any) => {
        this.checkingConflicts = false;
        if (response.success && response.data) {
          this.scheduleConflicts = response.data.conflicts || [];
          
          if (this.scheduleConflicts.length > 0) {
            this.toastService.warning(
              this.translate.instant('groups.scheduleConflictDetectedToast', { count: this.scheduleConflicts.length }),
              this.translate.instant('groups.scheduleConflictTitle'),
              5000
            );
          }
        }
      },
      error: (error: any) => {
        this.checkingConflicts = false;
        console.error('Failed to check schedule conflicts:', error);
      }
    });
  }

  async submit(): Promise<void> {
    if (this.form.invalid) return;
    
    if (this.scheduleConflicts.length > 0) {
      const conflictList = this.scheduleConflicts
        .map(c => `â€¢ ${c.groupName} (${c.groupCode}) - ${c.day}: ${c.time}`)
        .join('\n');
      
      const confirmed = await this.confirmation.confirm({
        title: this.translate.instant('groups.scheduleConflictWarning'),
        message: this.translate.instant('groups.scheduleConflictsMessage', { count: this.scheduleConflicts.length, conflictList }),
        confirmText: this.translate.instant('groups.yesProceed'),
        cancelText: this.translate.instant('groups.noCancel'),
        type: 'warning'
      });
      
      if (!confirmed) return;
    }
    
    this.submitting = true;
    // Use getRawValue() to include disabled fields (name and code)
    this.save.emit(this.form.getRawValue());
  }

  goBack(): void { this.router.navigate(['/dashboard/groups']); }
}


